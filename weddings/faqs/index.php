 	      <p><strong>How many people can you accommodate for a Civil Wedding Ceremony?</strong>
		  <p>We have a number of areas which are solemnised for civil wedding ceremonies, including our private dining room, pre-dinner drinks area and our restaurant. Our private dining room can accommodate a maximum number of 70 guests, seating for 50 and the rest standing.  Our bar and pre-dinner drinks area is able to accommodate larger parties up to 85, seating for 60 and the rest standing.</p>
 		  <p><strong>How many people can you accommodate for a sit-down meal?</strong></p>
		  <p>We are able to accommodate 40 guests in our private dining room on 4 round tables and 70 guests for a sit down meal in our marquee, situated across a top table and 6 round tables.</p>
 		  <p><strong>How many people can you accommodate for an evening reception?</strong></p>
		  <p>We are licensed to host evening receptions for a maximum of 160 guests.</p>
