		  <p>The Cock O' Barton is situated on the A534Wrexham to Nantwich link road about 8 miles outside of Wrexham. We are approximately 12 miles from Chester and 1.5 miles off the A41. We are less than a mile from Carden Park Hotel.</p>
 		  <p><strong>How much does it cost to hold a wedding at the Cock O' Barton?</strong></p>
		  <p>The Cock O' Barton is available to book throughout the year. There is no room hire cost, unless you opt for a marquee. The price varies from function to function depending on your needs. For more information please contact us on Tel: 01829 782277.</p>
 		  <p><strong>How many times do we see you prior to our wedding?</strong></p>
		  <p>We like to take the opportunity to see you many times before your big day. After you first make contact we can arrange to meet you for an initial chat and then work out all further meetings to suit your schedule.</p>
 		  <p><strong>Is it too late too organise my wedding?</strong></p>
		  <p>For those of you who are spur of the moment or who have been let down by somewhere else we can always react very quickly to your situation. No matter your timescale we can help you pull off a well organised celebration.</p>
