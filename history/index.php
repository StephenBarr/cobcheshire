      <p>As to the provenance of the Cock O Barton, old wives tales and local myths abound, it is commonly thought that the pub's name came from its association with a nearby cock fighting location. Although the pub did play some part in this activity, we think the name came from an earlier time.</p>
 	  <p>The area we call 'Barton' was supposedly named after a knight in the 900's who was rewarded for his services with land at the edge of the Carden Estate. Having built a manor house on the land a village developed around it. During the age of horse drawn transport, Barton became a convenient resting point on the Chester-Shrewsbury-London route.  The village prospered thanks to the increased traffic from coaches, farmers and travellers.</p>
