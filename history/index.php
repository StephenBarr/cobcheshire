    	  <p>The earliest references to an Inn at Barton date from 1603. It is likely that the name of this inn, the 'Cock O Barton', derived from the use of 'cock horses' that were hired out to provide carriages with extra pulling power around the local hills.</p>
 		  <p>As the business at that time would have offered rooms and food the Inn would have become well known amongst travellers and locals alike. John Hair, the previous landlord, told wonderful stories of Oliver Cromwell taking shelter here during the time that the Civil War neared Farndon and of ghosts that haunt the grounds from those troubled times!</p>
 		  <p>The advent of the railway that ran through Broxton would have seen the end of the horse and carriage business and it was not until the A534 was built that the Cock O Barton sat once more upon a major thoroughfare. In July 2006 the present owners undertook the task of refurbishing the Cock O Barton transforming and restoring the beautiful pub into the contemporary venue it is today.</p>
